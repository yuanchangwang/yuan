# ### Number( int float bool complex) 数值类型

# int 整型  (正整型  0  负整型)
intvar = 133
print(intvar)

# type 获取一个变量的类型 type(值)
res = type(intvar)
print(res)

# id  获取该变量所指向值的地址 id(值)
res = id(intvar)
print(res)


# 打印二进制
intvar = 0b101
print(intvar)
print(type(intvar))
print(id(intvar))


# 打印八进制
intvar = 0o127
print(intvar)
print(type(intvar))
print(id(intvar))

# 打印十六进制
intvar = 0Xff
print(intvar)
print(type(intvar))
print(id(intvar))






